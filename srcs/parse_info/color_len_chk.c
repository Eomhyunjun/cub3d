#include "../../cub3.h"

int		color_len_chk(int r, int g, int b)
{
	int	max;

	max = 255;
	if (0 > r || r > max)
		return (0);
	if (0 > g || g > max)
		return (0);
	if (0 > b || b > max)
		return (0);
	return (1);
}
